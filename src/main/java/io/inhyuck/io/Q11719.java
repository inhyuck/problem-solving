package io.inhyuck.io;

import java.io.*;
import java.util.*;

public class Q11719 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        while (scanner.hasNext()) {
            System.out.println(scanner.nextLine());
        }
    }
}
